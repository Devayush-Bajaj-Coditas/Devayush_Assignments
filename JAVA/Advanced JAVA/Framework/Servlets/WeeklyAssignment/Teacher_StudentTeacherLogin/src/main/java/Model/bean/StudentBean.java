package Model.bean;

public class StudentBean {
    private int stu_id;
    private String stu_firstName,stu_lastName, stu_password,stu_email,stu_mobileNo;

    public int getStu_id() {
        return stu_id;
    }

    public void setStu_id(int stu_id) {
        this.stu_id = stu_id;
    }

    public String getStu_firstName() {
        return stu_firstName;
    }

    public void setStu_firstName(String stu_firstName) {
        this.stu_firstName = stu_firstName;
    }

    public String getStu_lastName() {
        return stu_lastName;
    }

    public void setStu_lastName(String stu_lastName) {
        this.stu_lastName = stu_lastName;
    }

    public String getStu_password() {
        return stu_password;
    }

    public void setStu_password(String stu_password) {
        this.stu_password = stu_password;
    }

    public String getStu_email() {
        return stu_email;
    }

    public void setStu_email(String stu_email) {
        this.stu_email = stu_email;
    }

    public String getStu_mobileNo() {
        return stu_mobileNo;
    }

    public void setStu_mobileNo(String stu_mobileNo) {
        this.stu_mobileNo = stu_mobileNo;
    }
}
